# Repalcing
s1 = "Hello World!"
ns1 = s1.replace("World", "Python")
print(ns1)

# Concatenate
s2 = "Hello"
s3 = " World!"
print(s2 + s3)

# Repeate
s4 = "Hello World!"
print(s4 * 3)

# case convertion
s5 = "Hello World!"
ns5 = s5.upper()
print(ns5)

# strip()
s6 = " Hello, World! "
print(len(s6))
ns6 = s6.strip()
print(len(ns6))

# endswith()
s7 = "program.exe"
ns7 = s7.endswith("exe")
print(ns7)

# split()
s8 = "python is beginner friendly"
ns8 = s8.split()
print(ns8)

# join()
ns9=" ".join(ns8)
print(ns9)
